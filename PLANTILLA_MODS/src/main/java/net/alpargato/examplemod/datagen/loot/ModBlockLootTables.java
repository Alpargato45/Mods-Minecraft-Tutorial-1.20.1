import java.util.Set;

public class ModBlockLootTables extends BlockLootSubProvider {

    public ModBlockLootTables() {
        super(Set.of(), FeatureFlags.REGISTRY.allFlags());
    }

    @Override
    protected void generate() {
        //this.dropSelf(ModBlocks.BLOQUE_ZAFIRO.get());

        //this.add(ModBlocks.ORE_ZAFIRO.get(), block -> createCopperLikeOreDrops(ModBlocks.ORE_ZAFIRO.get(), ModItems.ZAFIRO.get()));
    }

    protected LootTable.Builder createCopperLikeOreDrops(Block pBlock, Item item) { //ESTO ES UN MÉTODO SACADO DE LOS MÉTODOS DE MINECRAFT VANILLA
        return createSilkTouchDispatchTable(pBlock, this.applyExplosionDecay(pBlock, LootItem.lootTableItem(item)
                .apply(SetItemCountFunction.setCount(UniformGenerator.between(2.0F, 5.0F))).apply(ApplyBonusCount.addOreBonusCount(Enchantments.BLOCK_FORTUNE))));
    }

    protected Iterable<Block> getKnownBlocks() {
        return ModBlocks.BLOCKS.getEntries().stream().map(RegistryObject::get)::iterator;
    }
}